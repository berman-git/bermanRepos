package tarea2;
/**
 * 1. Escribe un programa Java que te muestre 10 veces el texto: "Los bucles son muy sencillos"
 */
public class Ejercicio1 {

	public static void main(String[] args) {
		int i = 0;
		while (i < 10) {
			System.out.println("Los bucles son muy sencillos");
			i++;
		}
	}

}
